public class ObsResultadoTotal {

	String terminal;
	int cantidad;
	
	
	public String getTerminal() {
		return terminal;
	}


	public int getCantidad() {
		return cantidad;
	}


	public ObsResultadoTotal(String terminal, int cantidad) {
		super();
		this.terminal = terminal;
		this.cantidad = cantidad;
	}
	
	
}
