package tpdds;

public class GeoLocalizacion {
	
	Location latitud;
	Location longitud;
	
	public Location getLatitud() {
		return latitud;
	}
	public void setLatitud(Location latitud) {
		this.latitud = latitud;
	}
	public Location getLongitud() {
		return longitud;
	}
	public void setLongitud(Location longitud) {
		this.longitud = longitud;
	}

}
